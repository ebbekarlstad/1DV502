public class Song {
    // Attributes
    private String name;
    private int duration;

    // Setters
    public void setName(String name) {
        this.name = name;
    }
    public void setDuration(int duration) {
        this.duration = duration;
    }

    // Getters
    public String getName() {
        return this.name;
    }
    public int getduration() {
        return this.duration;
    }
}
